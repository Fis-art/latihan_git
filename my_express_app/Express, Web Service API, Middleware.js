//Apa itu Express.js

//Express.js adalah framework web app untuk Node.js yang ditulis dengan bahasa pemrograman JavaScript. 
// Framework open source ini dibuat oleh TJ Holowaychuk pada tahun 2010 lalu. 
// Express.js adalah framework back end. 
// Artinya, ia bertanggung jawab untuk mengatur fungsionalitas website, seperti pengelolaan routing dan session, permintaan HTTP, penanganan error, serta pertukaran data di server. 
// Framework yang satu ini punya arsitektur MVC (Model View Controller). Dengan begitu, setiap data diolah pada Model, dihubungkan melalui Controller, lalu ditampilkan menjadi informasi melalui View.

// Kelebihan dan Kekurangan Express.js
// Kelebihan 
// Bebas menentukan sendiri arsitektur dan struktur website yang akan dikembangkan

// Kekurangan
// Diperlukan penelusuran kode lebih untuk membuat, mengelola, dan merawat arsitektur website yang sudah dibuat sebelumnya dengan express

// Kelebihan
// Ukuran framework lebih kecil dan ringan, karena hanya berisi package inti

// Kekurangan
// Terlalu banyak pilihan plugin dan library untuk digunakan, bisa membingungkan bagi sebagian orang

// Kelebihan
// Kinerja website yang dihasilkan jadi lebih baik, mengingat ringannya framework

